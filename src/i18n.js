export function normalizeLanguage(language) {
  return resources[language] ? language : DEFAULT_LANGUAGE;
}

export function translate(language, key, params = {}) {
  const dictionary = resources[normalizeLanguage(language)] || resources[DEFAULT_LANGUAGE];
  const fallback = resources[DEFAULT_LANGUAGE];
  const template = getByPath(dictionary, key) ?? getByPath(fallback, key) ?? key;
  return interpolate(template, params);
}

function getByPath(source, path) {
  return String(path)
    .split(".")
    .reduce((current, part) => (current && Object.prototype.hasOwnProperty.call(current, part) ? current[part] : undefined), source);
}

function interpolate(template, params) {
  return String(template).replace(/\{([A-Za-z0-9_]+)\}/g, (match, key) =>
    Object.prototype.hasOwnProperty.call(params, key) ? String(params[key]) : match,
  );
}
